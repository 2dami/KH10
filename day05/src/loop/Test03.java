package loop;
//import java.lang.*;
public class Test03 {
public static void main(String[] args) {
		// 반복수의 활용
	//범위지정(ex : 1부터 100까지 출력)
	// 선언부 - 시작지점 지정  , 조건부 - 끝부분 지정
	for (int i=1; i <=100; i++) {
		System.out.println(i);
	}
//ex : 100부터 1까지 출력
 for(int i=100; i>=1; i--) {
	 System.out.println(i);
 }
 
 }
}
